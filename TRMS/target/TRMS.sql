CREATE TABLE TRFORMS(
USERNAME VARCHAR2(20),
NAMEE VARCHAR(20),
DATEE DATE,
GF VARCHAR2(12),
ET VARCHAR2(12),
GRADE VARCHAR2(2),
PASS VARCHAR2(1),
REIMBURSEMENT NUMBER,
DSSIG VARCHAR2(20),
DHSIG VARCHAR2(20),
BENCOSIG VARCHAR2(20),
SUBMITTED VARCHAR2(1),
FOREIGN KEY (DSSIG) REFERENCES APPROVERS(USERNAME),
FOREIGN KEY (DHSIG) REFERENCES APPROVERS(USERNAME),
FOREIGN KEY (BENCOSIG) REFERENCES APPROVERS(USERNAME),
FOREIGN KEY (USERNAME) REFERENCES EMPLOYEES(USERNAME));

CREATE TABLE EMPLOYEES(
USERNAME VARCHAR2(20),
NAMEE VARCHAR2(20),
PASSWORDD VARCHAR2(20),
AVAILABLE NUMBER,
AWARDED NUMBER,
PRIMARY KEY (USERNAME));

CREATE TABLE APPROVERS(
USERNAME VARCHAR2(20),
NAMEE VARCHAR2(20),
PASSWORDD VARCHAR2(20),
ROLEE NUMBER,
PRIMARY KEY (USERNAME));
